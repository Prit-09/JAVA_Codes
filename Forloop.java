 package Loops;

public class Forloop {

	public static void main(String[] args)
	{

		for(int a=1;a<=10;a++)
		{
			System.out.println(a + " ");
		}
        // for loop is used for when we know the exact output of the program means we know 
		//the range of condition means we know start and end
	}

}
