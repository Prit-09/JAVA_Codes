package Statements;

import java.util.Scanner;

public class Switchcase
{

	public static void main(String[] args)
	{
		char grade ='A';
		
		switch(grade)
		{
		
		case 1:
		{
			System.out.println("you have +65 marks");
		}break;
		case 2:
		{
			System.out.println("you have +55 marks");
		}break;
		default:
		{
			System.out.println("your fail");
			}
		}
	}

}


